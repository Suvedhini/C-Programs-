#include<iostream>
using namespace std;
int main(){
    int a;
    a=7;
    cout<<"The value of a is:"<<sizeof(a)<<"\n";

    float b=9.8;
    cout<<"The value of b is:"<<sizeof(b)<<"\n";

    char c;
    cout<<"The value of c is:"<<sizeof(c)<<"\n";

    bool d;
    cout<<"The value of d is:"<<sizeof(d)<<"\n";

    return 0;
}
